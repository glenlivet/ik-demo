package org.ikgroup.domain;

public class BindingProperty {
	
	private String value;
	
	public BindingProperty() {
		
	}

	public BindingProperty(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
}
